# upper()
print("Hello World".upper())

# lower()
print("Hello World".lower())

# title()
print("vári róbert".title())

# replace()
print("Hello World".replace("World", "Robert"))

# format()
print("My name is {}. I'm {} years old".format("Vári Róbert", 42))

# formated string
print(f"My name is {'Vári Róbert'}. I'm {42} years old.")